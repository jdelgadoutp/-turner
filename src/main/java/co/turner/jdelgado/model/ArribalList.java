package co.turner.jdelgado.model;

public class ArribalList {

	private NodeArribal head;
	private NodeArribal tail;
	private int size;

	public NodeArribal getHead() {
		return head;
	}

	public void setHead(NodeArribal head) {
		this.head = head;
	}

	public NodeArribal getTail() {
		return tail;
	}

	public void setTail(NodeArribal tail) {
		this.tail = tail;
	}

	public int getSize() {
		return size;
	}

	public void setSize(int size) {
		this.size = size;
	}

}
