package entity;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author ANDRES
 */
@Entity
@Table(name = "adviser")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Adviser.findAll", query = "SELECT a FROM Adviser a")
    , @NamedQuery(name = "Adviser.findById", query = "SELECT a FROM Adviser a WHERE a.id = :id")
    , @NamedQuery(name = "Adviser.findByName", query = "SELECT a FROM Adviser a WHERE a.name = :name")})
public class Adviser implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @Column(name = "id")
    private String id;
    @Column(name = "name")
    private String name;

    public Adviser() {
    }

    public Adviser(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Adviser)) {
            return false;
        }
        Adviser other = (Adviser) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entity.Adviser[ id=" + id + " ]";
    }
    
}
